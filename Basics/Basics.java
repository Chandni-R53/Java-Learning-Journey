package Basics;

public class Basics {
    public static void main(String[] args){
       //Data Types -> int,float,char,double,boolean
        int a=12;
        int b=3;
        int c=a+b;
        System.out.print("Integer sum=");//this prints the output and cursor stays on the same line
        System.out.println(c);//this prints the output and cursor moves to the next line
        float percent=84.2f;
        boolean yes=true;
        char ch='G';
        double num=244.43;
        System.out.print("Percentage in % =");
        System.out.println(percent);
        System.out.print("Yes=");
        System.out.println(yes);
        System.out.print("Char=");
        System.out.println(ch);
        System.out.print("Double=");
        System.out.println(num);
    }
}
